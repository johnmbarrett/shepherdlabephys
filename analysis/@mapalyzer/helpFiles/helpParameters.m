	Help on using the CELL PARAMETERS panel
    
    This panel gives you the option to analyze cell parameters for general physiology 
    traces.
    
    Essentially the same analysis is automatically performed with input map analysis (if 
    you check the 'with Rs,Rm' checkbox), and it is unnecessary to repeat the analysis 
    in this panel. 
    
    The average results are displayed in the text windows, and the data are plotted in
    a figure.
    
    The 'Pulse' parameters allow you to specify the stimulus pulse parameters.
    
    The 'Skipping' parameter lets you analyze every Nth trace, to save time. Set this to 
    a value of 1 if you want to evaluate every trace.
    
